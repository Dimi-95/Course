#include <iostream>

using std::cout;
using std::cin;
using std::endl;

int main()
{
    //Heap allocation
    int* p_zahl = new int { 4 };

    //The memory adress of the heap variable, where the pointer points to
    cout << p_zahl << endl;
    //The value of the heap variable, where the pointer points to
    cout << *p_zahl << endl;
    //Memory adress of the pointer itself
    cout << &p_zahl << endl;

    //Heap de-allocation
    delete p_zahl;

    return 0;
}
