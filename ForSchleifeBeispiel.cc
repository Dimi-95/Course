#include <iostream>

using std::cout;
using std::cin;
using std::endl;

int main()
{
    double input_number;
    int num_iterations;


    cout << "Bitte gebe eine Zahl ein: ";
    cin >> input_number;
    cout << endl;
    cout << "Wie viele Iterationen willst du haben? ";
    cin >> num_iterations;
    cout << endl;

    for ( int i = 0; i < num_iterations; i++)
    {
        input_number = input_number * input_number;
        cout << "Iteration: " << i << " number: " << input_number << endl;
    }

    return 0;
}
